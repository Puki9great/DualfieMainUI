    public void onCreate(Bundle savedInstanceState) {

        super.onCreate(savedInstanceState);


        UIHandler = new Handler(new Handler.Callback() {
            @Override
            public boolean handleMessage(Message message) {
                switch (message.what) {
                    case Constants.MESSAGE_READ:
                        //inputstream on
                        Log.d(TAG, "Control parsing.. ");

                        byte[] readBuf = (byte[]) message.obj;
                        String control = readBuf.toString();
                        switch (control) {
                            case Constants.STOP_CAMERA:
                                closeCamera();
                            case Constants.TAKE_PICTURE:
                                takePicture();
                            case Constants.FETCH:

                                if(mState == STATE_PICTURE_TAKEN && capturedImage != null)
                                {
                                    comm.write(capturedImage);
                                }
                                capturedImage=null;
                        }
                    case Constants.MESSAGE_WRITE:
                        break;

                    case Constants.MESSAGE_STATE_CHANGE:
                        Log.d(TAG, "State Change " + message.arg1);

                        switch (message.arg1) {
                            case Constants.STATE_CONNECTED:
                                Log.d(TAG, "handleMessage: Connected");
                                break;
                            case Constants.STATE_CONNECTING:

                                break;
                            case Constants.STATE_LISTEN:
                            case Constants.STATE_NONE:

                                break;
                        }
                        break;

                    case Constants.ACTION_CAPTURED:
                        capturedImage = (Bitmap) message.obj;

                }
                return true;
            }
        });

        comm = new BluetoothComm(getContext(), UIHandler);
        comm.start();
    }

    @Override
    public void onResume() {
        super.onResume();
        if (comm != null) {
            if (comm.getState() == Constants.STATE_NONE) {
                comm.start();
            }
        }
        startBackgroundThread();
        if (mTextureView.isAvailable()) {
            openCamera(mTextureView.getWidth(), mTextureView.getHeight());
        } else {
            mTextureView.setSurfaceTextureListener(mSurfaceTextureListener);
        }
    }

    @Override
    public void onPause() {
        closeCamera();
        stopBackgroundThread();
        super.onPause();
    }

    @Override
    public void onDestroy() {
        Log.d(TAG, "onDestroy");
        super.onDestroy();
        if (comm != null) {
            comm.stop();
        }
    }


